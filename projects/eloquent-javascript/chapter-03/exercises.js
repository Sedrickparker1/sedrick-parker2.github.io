////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a, b) {
  return a < b ? a: b;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
   return num % 2 == 0;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, letter) {
  let array = str.split('');
  
  return array.reduce((count, currVal) =>{
    if (currVal === letter){
      count += 1;
    }
    return count
  } ,0)
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let bean = 'B';
  let text = string.split('');

  return text.reduce( (counter, currVal) =>{
    if (currVal === bean){
      counter += 1;
    }
    return counter;
  }, 0)
}

